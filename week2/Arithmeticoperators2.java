package week2;

public class Arithmeticoperators2 {

	public static void main(String[] args) {
		int i =10;
		System.out.println(i++);
		System.out.println(i);
		
		int j = 100;
		System.out.println(++j);
		System.out.println(j);
		
		System.out.println("---------------------");
		
		int ii = 10;
		System.out.println(ii--);
		System.out.println(ii);
		
		int jj = 100;
		System.out.println(--jj);
		System.out.println(jj);
		
		System.out.println("*******************");
		
		
		int x = 10;
		int y = 20;
		
		x+=10;
		System.out.println(x);
		y-=10;
		System.out.println(y);
		
		x*=10;
		System.out.println(x);
		
		y/=2;
		System.out.println(y);
		
		
		y%=2;
		System.out.println(y);
		
		
		
		
		
		
		
		

	}

}
